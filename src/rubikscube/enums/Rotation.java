package rubikscube.enums;

/*
 * 
 * Definit les mouvements possibles sur le rubiks selon la notation
 * de Singmaster.
 * U <=> Up
 * D <=> Down
 * L <=> Left
 * R <=> Right
 * F <=> Front
 * B <=> Back
 * 
 * Ui <=> Up'
 * etc.
 */

public enum Rotation {
	L, Li,
	B, Bi,
	R, Ri,
	F, Fi,
	U, Ui,
	D, Di;
}
